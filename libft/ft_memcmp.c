#include <string.h>

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*st1;
	unsigned char	*st2;
	size_t			i;

	st1 = (unsigned char *)s1;
	st2 = (unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (i < n)
	{
		if (st2[i] != st1[i])
			return (st1[i] - st2[i]);
		i++;
	}
	return (st1[i - 1] - st2[i - 1]);
}
