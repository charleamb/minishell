#include <unistd.h>

void	ftpu(char x, int fd)
{
	write(fd, &x, 1);
}

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ftpu('-', fd);
		ftpu('2', fd);
		n = 147483648;
	}
	if (n < 0)
	{
		ftpu('-', fd);
		n = -n;
	}
	if (n > 9)
		ft_putnbr_fd(n / 10, fd);
	ftpu(n % 10 + '0', fd);
}
