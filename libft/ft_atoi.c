int	ft_atoi(const char *str)
{
	unsigned int	i;
	int				p;
	unsigned int	k;

	p = 1;
	i = 0;
	k = 0;
	while ((str[i] == '\t' || str[i] == '\n' || str[i] == '\v' || str[i] == '\f'
			|| str[i] == '\r' || str[i] == ' ') && str[i] != '\0')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			p = -p;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = (k * 10) + str[i] - '0';
		i++;
	}
	k = k * p;
	return (k);
}
